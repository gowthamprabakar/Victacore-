// RadiusTokens.swift
// VitaCoreDesign — Corner-radius tokens

import SwiftUI

// MARK: - VCRadius

/// Corner-radius constants for VitaCore UI elements.
public enum VCRadius {
    /// 8 pt — small chips, badges, mini cards.
    public static let sm: CGFloat = 8
    /// 12 pt — standard inner cards, input fields.
    public static let md: CGFloat = 12
    /// 18 pt — primary cards, modals.
    public static let lg: CGFloat = 18
    /// 22 pt — hero cards, large panels.
    public static let xl: CGFloat = 22
    /// 100 pt — pill / capsule shape.
    public static let pill: CGFloat = 100
}
